export type CompensationType =
  | "paid"
  | "gifted"
  | "affiliate"
  | "revenue_share"
  | "unpaid";

export type CampaignStatus =
  | "draft"
  | "active"
  | "paused"
  | "closed"
  | "completed";

export type AssetType =
  | "product_photo"
  | "brand_asset"
  | "campaign_brief"
  | "reference"
  | "brand_guidelines";

// ─────────────────────────────────────────────────────────────────────────────
// Database row types
// ─────────────────────────────────────────────────────────────────────────────

export interface Campaign {
  id: string;
  user_id: string;
  // Business identity (embedded — no separate business_profiles table in V1)
  business_name: string;
  business_industry: string | null;
  business_website: string | null;
  business_instagram: string | null;
  business_tiktok: string | null;
  business_location: string | null;
  // Campaign details
  title: string;
  description: string;
  product_service: string | null;
  campaign_goal: string | null;
  // Compensation — the most important field for creators
  compensation_type: CompensationType;
  compensation_amount_fixed: number | null;
  compensation_budget_min: number | null;
  compensation_budget_max: number | null;
  compensation_per_deliverable: number | null;
  // Creator requirements
  required_niches: string[];
  min_followers: number | null;
  required_country: string | null;
  required_language: string | null;
  required_platforms: string[];
  deadline: string | null;
  // Status
  status: CampaignStatus;
  is_published: boolean;
  created_at: string;
  updated_at: string;
  // Joined relations
  deliverables?: CampaignDeliverable[];
  assets?: CampaignAsset[];
}

export interface CampaignDeliverable {
  id: string;
  campaign_id: string;
  platform: string;
  content_type: string;
  quantity: number;
  notes: string | null;
  display_order: number;
}

export interface CampaignAsset {
  id: string;
  campaign_id: string;
  asset_type: AssetType;
  url: string;
  name: string | null;
  display_order: number;
  created_at: string;
}

// ─────────────────────────────────────────────────────────────────────────────
// Campaign creation form state
