export function formatBudget(campaign: Campaign): string {
  const { compensation_type, compensation_amount_fixed, compensation_budget_min, compensation_budget_max, compensation_per_deliverable } = campaign;
  if (compensation_type !== "paid") return COMPENSATION_LABELS[compensation_type];
  if (compensation_amount_fixed) return `$${compensation_amount_fixed.toLocaleString()}`;
  if (compensation_budget_min && compensation_budget_max) return `$${compensation_budget_min.toLocaleString()} – $${compensation_budget_max.toLocaleString()}`;
  if (compensation_budget_min) return `From $${compensation_budget_min.toLocaleString()}`;
  if (compensation_per_deliverable) return `$${compensation_per_deliverable.toLocaleString()} / deliverable`;
  return "Paid";
}

export function compensationColor(type: CompensationType): { text: string; bg: string; border: string } {
  switch (type) {
    case "paid":          return { text: "oklch(0.72 0.14 152)", bg: "oklch(0.72 0.14 152 / 12%)", border: "oklch(0.72 0.14 152 / 30%)" };
    case "gifted":        return { text: "oklch(0.78 0.12 60)",  bg: "oklch(0.78 0.12 60 / 12%)",  border: "oklch(0.78 0.12 60 / 30%)"  };
    case "affiliate":     return { text: "oklch(0.72 0.1 250)",  bg: "oklch(0.72 0.1 250 / 12%)",  border: "oklch(0.72 0.1 250 / 30%)"  };
    case "revenue_share": return { text: "oklch(0.72 0.1 290)",  bg: "oklch(0.72 0.1 290 / 12%)",  border: "oklch(0.72 0.1 290 / 30%)"  };
    case "unpaid":        return { text: "oklch(1 0 0 / 40%)",   bg: "oklch(1 0 0 / 5%)",          border: "oklch(1 0 0 / 12%)"         };
  }
}
